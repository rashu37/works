package com.chrome.things.service;

public class Instagram {
	
	public String user;
	public String userName;
	public int age;
	
	public Instagram() {
		System.out.println("No arguments constructor for Instagram");
	}

	public Instagram(String user, String userName, int age) {
		super();
		this.user = user;
		this.userName = userName;
		this.age = age;
	}
	
	

}
